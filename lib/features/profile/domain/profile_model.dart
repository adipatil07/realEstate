class User {
  final String name;
  final String email;
  final String username;
  final String phone;
  final String address;
  final int propertiesListed;
  final int favorites;

  User({
    required this.name,
    required this.email,
    required this.username,
    required this.phone,
    required this.address,
    required this.propertiesListed,
    required this.favorites,
  });
}
